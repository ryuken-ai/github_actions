
def test_calc_addition():
    """Test le résultat de 2 + 4"""
    output = 2 + 4
    assert output == 6


def test_calc_substraction():
    """Test le résultat de 2 - 4"""
    output = 2 - 4
    assert output == -2


def test_calc_multiply():
    """Test le résultat de 2 * 4"""
    output = 2 * 4
    assert output == 8


